package io.github.lengors.init_sources_maven_plugin;

/**
 * Enum representing a specific type of source root to manage within the Maven project.
 *
 * @author lengors
 */
public enum SourceType {
  /**
   * Compile source roots of the Maven project.
   */
  COMPILE,

  /**
   * Test compile source roots of the Maven project.
   */
  TEST_COMPILE
}
